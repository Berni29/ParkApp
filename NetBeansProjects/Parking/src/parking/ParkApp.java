    private void outButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_outButtonActionPerformed
        // TODO add your handling code here:
        try{
            feeValue.setText(String.format("%.2f", area.exitArea(outPlate.getText())));
            Predicate<String> p = (x) -> x.split(" ")[0].equals(outPlate.getText());
            data.removeIf(p);
            listOfVehicles.setListData(data.toArray(new String[0]));
            fillBar.setValue(100/areaSize*area.getFreeSpots());
        }
        catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        finally{
            outPlate.setText("");
        }
    }//GEN-LAST:event_outButtonActionPerformed

